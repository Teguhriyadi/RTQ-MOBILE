  List<Map> list = [];
  for (int i = 0; i < dataNilai.length; i++) {
    if (dataNilai[i]['id_pelajaran'] == idPelajaran &&
        dataNilai[i]['nis_santri'] == nis) {
      list.add(dataNilai[i]);
    }
  }
  return list;
}

